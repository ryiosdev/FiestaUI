import Foundation

/// An enum representing the spacing and sizing metrics that belong the Fiesta Theme.
public enum FiestaSize {
    
    /// A standard padding between SwiftUI elements for the Fiesta Theme
    public static let paddingNormal = CGFloat(8.0)
    
    /// CornerSmall to be used for Button
    public static let cornerSmall = CGFloat(5.0)
}
